//! Custom Font Library for Genesis
//!
//! Inspired by Steve Jobs's vision: custom fonts that define the look and feel
//! of the operating system. Each character is hand-crafted (or AI-generated)
//! to create a unique, "wacko and crude" aesthetic that's distinctly Genesis.

use alloc::collections::BTreeMap;
use alloc::string::String;

/// Font character bitmap (8x8 pixels)
/// Each byte represents one row, with bits representing pixels
/// Bit 7 (MSB) is the leftmost pixel, bit 0 is rightmost
pub type CharBitmap = [u8; 8];

/// A complete font containing all character glyphs
#[derive(Clone)]
pub struct Font {
    /// Font name
    pub name: &'static str,
    /// Character width in pixels
    pub char_width: u32,
    /// Character height in pixels
    pub char_height: u32,
    /// Character spacing (pixels between characters)
    pub spacing: u32,
    /// Map of character to bitmap
    pub glyphs: BTreeMap<char, CharBitmap>,
}

impl Font {
    /// Create a new font
    pub fn new(name: &'static str, char_width: u32, char_height: u32, spacing: u32) -> Self {
        Font {
            name,
            char_width,
            char_height,
            spacing,
            glyphs: BTreeMap::new(),
        }
    }

    /// Add a glyph to the font
    pub fn add_glyph(&mut self, ch: char, bitmap: CharBitmap) {
        self.glyphs.insert(ch, bitmap);
    }

    /// Get bitmap for a character, or None if not found
    pub fn get_glyph(&self, ch: char) -> Option<&CharBitmap> {
        self.glyphs.get(&ch)
    }

    /// Get bitmap for a character, or return a default (space) if not found
    pub fn get_glyph_or_default(&self, ch: char) -> &CharBitmap {
        self.glyphs.get(&ch).unwrap_or(&[0; 8])
    }
}

