/// Format for AI-generated font data
/// 
/// This structure can be used to import fonts generated by AI.
/// The AI can generate a JSON or binary format with character bitmaps.
#[derive(Debug, Clone)]
pub struct AIGeneratedFont {
    /// Font name
    pub name: String,
    /// Description/style
    pub style: String,
    /// Character bitmaps (char -> bitmap)
    pub glyphs: BTreeMap<char, CharBitmap>,
}

impl AIGeneratedFont {
    /// Convert AI-generated font to a Font
    /// 
    /// Note: This creates a font with a temporary name. For persistent fonts,
    /// use Font::new() directly with a static string.
    pub fn to_font(&self) -> Font {
        // Create font with a default name since we can't use &self.name
        // In practice, AI-generated fonts should be created directly as Font
        let mut font = Font::new("AI Generated", 8, 8, 1);
        for (ch, bitmap) in &self.glyphs {
            font.add_glyph(*ch, *bitmap);
        }
        font
    }
}

/// Global font instance
use spin::Mutex;
use lazy_static::lazy_static;

lazy_static! {
    /// Current active font
    pub static ref CURRENT_FONT: Mutex<Font> = Mutex::new(create_genesis_font());
}

/// Get the current font
pub fn get_font() -> Font {
    CURRENT_FONT.lock().clone()
}

/// Set a new font
pub fn set_font(font: Font) {
    *CURRENT_FONT.lock() = font;
}

/// Get glyph for a character using the current font
pub fn get_glyph(ch: char) -> CharBitmap {
    CURRENT_FONT.lock().get_glyph_or_default(ch).clone()
}

